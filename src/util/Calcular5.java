package util;

public class Calcular5 {

	private float n4;
	private float n5;

	
	public float calcula() {
		return (float)getN4() - getN5();
	}
	
	
	public float getN4() {
		return n4;
	}
	public void setN4(float n4) {
		this.n4 = n4;
	}
	public float getN5() {
		return n5;
	}
	public void setN5(float n5) {
		this.n5 = n5;
	}
	

}
